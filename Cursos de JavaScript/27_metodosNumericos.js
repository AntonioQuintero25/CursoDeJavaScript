//O objeto Number e pai dos números, e contém métodos muito úteis para usar.

//parseFloat

console.log(parseFloat('12.999'));
console.log(Number.parseFloat('12.999'));

//parseInt

console.log(parseInt('12.999'));
console.log(parseInt(13.999));
console.log(Number.parseInt('28.999'));

//toFixed

console.log(23.7776.toFixed(2));

//isNaN
console.log(isNaN("Teste"));
console.log(isNaN(12));

//MAX_VALUE e MIN_VALUE
console.log(Number.MAX_VALUE);
console.log(Number.MIN_VALUE);